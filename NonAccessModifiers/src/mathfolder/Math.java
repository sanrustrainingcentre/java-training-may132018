package mathfolder;

public class Math {
	
	public static final double PI = (22.0 / 7); 
	
	public static int add(int x, int y)
	{
		return (x + y);
	}
	
	public static int add(int x, int y, int z)
	{
		return (x + y + z);
	}
	
	public int add(int x, int y, int z, int a)
	{
		return (x + y + z + a);
	}

}
