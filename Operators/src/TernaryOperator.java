
public class TernaryOperator {

	public static void main(String[] args) {
		
		int x = 10;
		
		int z = x > 10 ? x-- : x++;
//		int z = x > 10 ? 3 : 2;
		
		
		System.out.println(100);
		System.out.println("z = " + z);
		System.out.println("x = " + x);
		
		String value = "Result = " + (2 + 2);
		System.out.println(value);
		
		
		int a = 11;
		double b = ((double) a / 2);
		System.out.println(b);

	}

}
