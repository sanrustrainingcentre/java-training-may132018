
public class PrimitiveDataTypesDemo {

	public static void main(String[] args) {
		
		//Single line commenting
		//variable declarations
		byte b;//b is a variable name of 'byte' data type, which is going to hold 8 bits of information
		short s;
		int i;
		long l;
		float f;
		double d;
		boolean result;
		char ch;
		
		char hindiCh = '\u0905';
		
		//initialization of variables
		b = 127;
		s = 1000;
		i = 2345567;
		l = 1234567889999988L;
		f = 0.5123456789123456789123456789f;
		d = 12345678.123456789876543212345674567;
		result = true;
		ch = '&';
		
		//declaration and initialization of a variable in one statement
		int x = 25;
		
		System.out.println("byte b => " + b);
		System.out.println("short s => " + s);
		System.out.println("int i => " + i);
		System.out.println("long l => " + l);
		System.out.println("float f => " + f);
		System.out.println("double d => " + d);
		System.out.println("boolean result => " + result);
		System.out.println("char ch => " + ch);
		
		System.out.println("char hindiCh => " + hindiCh);
		
		

	}

}
